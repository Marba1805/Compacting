

struct comLet {
   char let;
   int qtd;
};




void compactador(v){
	bool i = false;
	while(i != true){
		for(int c = 0; c<= len(v); c = c + 1){
			for(int j = 0; j<= len(v); j = j + 1)
			var.let = v[c];
			if (v[j] == var.let){
				var.qtd = var.qtd + 1;
			}
			else{
				break;
			}
		}
	}
}
